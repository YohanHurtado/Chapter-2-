colombian_cities = ['bogotá', 'medellín', 'cali', 'barranquilla', 'cartagena']

alphabetical_order = sorted(colombian_cities)
print(alphabetical_order)

reverse_alphabetical_order = sorted(colombian_cities, reverse=True)
print(reverse_alphabetical_order)

colombian_cities.reverse()
print(colombian_cities)

colombian_cities.reverse()
print(colombian_cities)

colombian_cities.sort()
print(colombian_cities)

colombian_cities.sort(reverse=True)
print(colombian_cities)